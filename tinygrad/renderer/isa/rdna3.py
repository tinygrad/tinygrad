def alu(ctx, x:UOp):
  dpreciz = x.dtype.itemsize == 8
  ins = None
  if len(x.src) == 1:
    if x.op is Ops.SQRT: ins = V_SQRT[x.dtype]
    elif x.op is Ops.LOG2: ins = V_LOG[x.dtype]
    elif x.op is Ops.EXP2: ins = V_EXP[x.dtype]
    elif x.op is Ops.SIN: ins = V_SIN[x.dtype]
    elif x.op is Ops.TRUNC: ins = V_TRUNC[x.dtype]
    elif x.op is Ops.RECIPROCAL: ins = V_RCP[x.dtype]
  else: 
    a,b = x.src
    def _bitwise(ins): return bitwise64(ctx, x, ins) if dpreciz else _vop2(ctx, x.ins(ins))
    # how to handle boolean dtype?
    # ex. AND between 2 bools outputting a bool, should the be an salu op?
    # - cmp outputs are automatically sgprs
    # NOTE: booleans should be natively represented as vcc/scc
    if x.op is Ops.ADD: # TODO: handle signed?
      if dpreciz: return add64(x)
      ins = V_ADD[x.dtype]
    elif x.op is Ops.MUL:
      if dpreciz: return mul64(ctx,x)
      ins = V_MUL[x.dtype]
    elif x.op is Ops.AND: return _bitwise(RDNA3Ops.v_and_b32_e32)
    elif x.op is Ops.OR: return _bitwise(RDNA3Ops.v_or_b32_e32)
    elif x.op is Ops.XOR: return _bitwise(RDNA3Ops.v_xor_b32_e32)
    elif x.op is Ops.SHL:
      x = x.replace(src=x.src[::-1])
      ins = RDNA3Ops.v_lshlrev_b64 if dpreciz else RDNA3Ops.v_lshlrev_b32_e32 
    elif x.op is Ops.SHR:
      x = x.replace(src=x.src[::-1])
      ins = RDNA3Ops.v_lshrrev_b64 if dpreciz else RDNA3Ops.v_lshrrev_b32_e32
  if ins is None:
    raise NotImplementedError(f"alu optype not implemented. op={x.op}, is_unary={len(x.src)==1}")
  return x.ins(ins) if len(x.src) == 1 else _vop2(ctx, x.ins(ins))

