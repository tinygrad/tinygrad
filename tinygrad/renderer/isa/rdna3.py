def add64(ctx, x:UOp):
  if dtypes.is_float(x.dtype): return x.ins(V_ADD[x.dtype]) # f64 add is native
  a1, a2 = x.ins(RDNA3Ops.v_add_co_u32), x.ins(RDNA3Ops.v_add_co_ci_u32)
  return a2.after(a1)

