#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd -- "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

export LOGMLPERF=1
export MODEL="llama2_70b_lora"
export SUBMISSION_PLATFORM="tinybox_8xMI350X"
export SEED="${SEED:-1234}"
export DATA_SEED="${SEED}"

DATETIME="$(date "+%m%d%H%M")"
LOGFILE="${LOGFILE:-llama2_70b_lora_8xMI350x_${DATETIME}_${SEED}.log}"

COMMON_ENV=(
  DEV=AMD
  EMULATE=AMD_CDNA4
  CHECK_OOB=0
  REWRITE_STACK_LIMIT=5000000
  HCQDEV_WAIT_TIMEOUT_MS=240000
  DEVICE_IN_FUNCTION_BUG=1
  DEBUG=0
  HK_FLASH_ATTENTION=1
  ALL2ALL=1
  USE_ATOMICS=0
  ASM_GEMM=1
  WQKV=1
  OFFLOAD_OPTIM=1
  DEFAULT_FLOAT=bfloat16
  OPTIM_DTYPE=bfloat16
  MP=8
  DP=1
  BS=1
  EVAL_BS=1
  GRADIENT_ACC_STEPS=1
  SEQLEN=8192
  MAX_STEPS=1024
  EVAL_FREQ=48
  EVAL_TARGET=0.925
  LR=4e-4
  END_LR=0.0
  WARMUP_STEPS=0
  LLAMA_LORA_RANK=16
  LLAMA_LORA_ALPHA=32
  LLAMA_LORA_DROPOUT=0.1
  JITBEAM=3
  BEAM_UOPS_MAX=6000
  BEAM_UPCAST_MAX=256
  BEAM_LOCAL_MAX=1024
  BEAM_MIN_PROGRESS=5
  BEAM_PADTO=1
)

# init / beam search warmup on a reduced layer count. Keep benchmark identity and sequence length intact.
env -u RUNMLPERF -u LOAD_CKPT -u RESUME_CKPT -u TRAIN -u CKPT -u FP8 -u TRAIN_ON_VAL -u SMALL -u SAMPLES -u EVAL_SAMPLES -u LLAMA_BASE_QUANTIZE \
  "${COMMON_ENV[@]}" DATASET_PATH="" MODEL_PATH="" TOKENIZER_PATH="" FAKEDATA=1 BENCHMARK=10 INITMLPERF=1 LLAMA_LAYERS=2 \
  "${SCRIPT_DIR}/dev_run.sh" 2>&1 | tee "${LOGFILE}"

env -u INITMLPERF -u FAKEDATA -u BENCHMARK -u LLAMA_LAYERS -u FULL_LAYERS -u TRAIN -u CKPT -u FP8 -u TRAIN_ON_VAL -u SMALL -u LOAD_CKPT -u RESUME_CKPT -u SAMPLES -u EVAL_SAMPLES -u LLAMA_BASE_QUANTIZE \
  "${COMMON_ENV[@]}" RUNMLPERF=1 DATASET_PATH="${DATASET_PATH:-/raid/datasets/scrolls_gov_report_8k}" MODEL_PATH="${MODEL_PATH:-/raid/weights/llama2-70b-fused-qkv-mlperf}" \
  "${SCRIPT_DIR}/dev_run.sh" 2>&1 | tee -a "${LOGFILE}"
