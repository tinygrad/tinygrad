  k.emit(v_mul_lo_u32(v[3], v[1], s[36]))
  k.emit(v_and_b32_e32(v[0], 1, v[4]))
  k.emit(v_mul_lo_u32(v[0], 16, v[0]))
  k.emit(v_and_b32_e32(v[5], 15, v[134]))
  k.emit(v_add_lshl_u32_e64(v[0], v[5], v[0], 3))
  k.emit(s_mul_i32(s[8], 256, s[2]))
  k.emit(v_add_u32_e32(v[0], s[8]))
  k.emit(s_mul_i32(s[8], 256, s[3]))
  k.emit(v_add_u32_e32(v[1], s[8], v[1]))
  k.emit(v_add_lshl_u32_e64(v[11], v[3], v[0], 1))

  # acc vgprs to normal vgprs
  # Pattern: 4 columns of acc regs (stride 4), mapped to contiguous VGPR ranges
  for i in range(64): k.emit(v_accvgpr_read(v[16 + i], v[i * 4]))       # col 0: acc[0,4,8...] -> v[16:79]
  for i in range(48): k.emit(v_accvgpr_read(v[80 + i], v[1 + i * 4]))   # col 1 part 1: acc[1,5,9...] -> v[80:127]
  for i in range(16): k.emit(v_accvgpr_read(v[136 + i], v[193 + i * 4]))# col 1 part 2: acc[193,197...] -> v[136:151]
  for i in range(64): k.emit(v_accvgpr_read(v[152 + i], v[2 + i * 4]))  # col 2: acc[2,6,10...] -> v[152:215]
  for i in range(32): k.emit(v_accvgpr_read(v[216 + i], v[3 + i * 4]))  # col 3 part 1: acc[3,7,11...] -> v[216:247]

  k.emit(v_mov_b32_e32(v[8], 4294901760))
  k.emit(v_mov_b32_e32(v[9], 2147418112))
  k.emit(v_mov_b32_e32(v[10], 32767))

  # convert f32 acc pairs to packed fp16/bf16 and store
  # packed cast only exists in cdna4, use the unpacked cast on cdna3
  store_bases = list(range(16, 128, 8)) + list(range(136, 248, 8))
  for i, base in enumerate(store_bases):
    for j in range(4): k.emit(v_cvt_out(v[base + j], v[base + j*2], v[base + j*2 + 1]))
    # pointer update (skip for first store)
    if i > 0:
      k.emit(s_lshl_b32(s[12], s[36], 1))
      k.emit(s_add_u32(s[16], s[16], s[12]))
      k.emit(s_addc_u32(s[17], s[17], 0))
    k.emit(buffer_store_dwordx4(v[base:base+3], v[11], s[16:19], 0, 0, 1, 0, 0, 0, 0, 0, 1))

  k.emit(s_nop())

  # Read remaining accumulators (col 3 part 2)
  for i in range(32): k.emit(v_accvgpr_read(v[16 + i], v[131 + i * 4]))  # acc[131,135,139...] -> v[16:47]

  k.emit(v_mov_b32_e32(v[8], 4294901760))
  k.emit(v_mov_b32_e32(v[9], 2147418112))
  k.emit(v_mov_b32_e32(v[10], 32767))

  # Convert and store
  for i in range(4):
    base = 16 + i * 8
    for j in range(4): k.emit(v_cvt_out(v[base + j], v[base + j*2], v[base + j*2 + 1]))
    k.emit(s_lshl_b32(s[12], s[36], 1))
    k.emit(s_add_u32(s[16], s[16], s[12]))
    k.emit(s_addc_u32(s[17], s[17], 0))
    k.emit(buffer_store_dwordx4(v[base:base+3], v[11], s[16:19], 0, 0, 1, 0, 0, 0, 0, 0, 1))
  k.emit(s_nop())
  k.emit(s_endpgm())
  return k
